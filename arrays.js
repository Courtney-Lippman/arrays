var pets = ["dog", "cat", "fish", "horse"];
/* pop() method removes the last element("horse") from an array and returns that
 element.*/
pets.pop();
console.log(pets);



var ages = [1, 16, 55, 87]
/* push() method adds one or more elements (22) to the end of an array and
returns the length of the new array.*/
ages.push(22);
console.log(ages);

var trueFalse = [true, false, true, false]
/* shift() method removes the first element (true) from an array and returns
 that removed element. */
trueFalse.shift();
  console.log (trueFalse);

// Index Positions
/* An index position is the number that corresponds with the elements position
 in an array. */
 var num = [1,2,3,4]

 console.log(num[0]);
 // output is 1

 console.log(num[1]);
 // output is 2

 console.log(num[2]);
 // output is 3

 console.log(num[3]);
 // output is 4
